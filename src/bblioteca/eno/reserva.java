    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        functions func = new functions();
        String nome_aluno = jTextField2.getText();
        String nome_livro = jTextField1.getText();
        String curso = jTextField3.getText();
        String data = jTextField4.getText();
        String rd1 = jRadioButton4.getText();
        String rd2 = jRadioButton5.getText();
        String rd3 = jRadioButton6.getText();
        ResultSet rs = null;
        if (jRadioButton4.isSelected()) {
            rs = func.execute("SELECT qtd FROM qtdreserva WHERE turno='" + rd1 + "';", "SELECT");
        } else if (jRadioButton5.isSelected()) {
            rs = func.execute("SELECT qtd FROM qtdreserva WHERE turno='" + rd2 + "'; ", "SELECT");
        } else if (jRadioButton6.isSelected()) {
            rs = func.execute("SELECT qtd FROM qtdreserva WHERE turno='" + rd3 + "';", "SELECT");
        }
        int quant_antigo2 = 0;
        try {
            while (rs.next()) {
                quant_antigo2 = rs.getInt("qtd");
            }
            if (jRadioButton4.isSelected()) {
                int res = quant_antigo2 + 1;
                func.execute("UPDATE qtdreserva SET qtd=" + res + " where turno='" + rd1 + "';", "INSERT");
                jRadioButton4.setSelected(false);

            } else if (jRadioButton5.isSelected()) {
                int res = quant_antigo2 + 1;
                func.execute("UPDATE qtdreserva SET qtd=" + res + " where turno='" + rd2 + "';", "INSERT");
                jRadioButton5.setSelected(false);

            } else if (jRadioButton6.isSelected()) {
                int res = quant_antigo2 + 1;
                func.execute("UPDATE qtdreserva SET qtd=" + res + " where turno='" + rd3 + "';", "INSERT");
                jRadioButton6.setSelected(false);
            } 
        } catch (Exception e) {
            System.out.println("" + e);
        }
        if (nome_aluno.equals("") || nome_livro.equals("") || curso.equals("") || jRadioButton4.isSelected() == false && jRadioButton5.isSelected() == false && jRadioButton6.isSelected() == false) {
            JOptionPane.showMessageDialog(this, "Digite os dados para reservar!");
        } else {
            func.execute("INSERT INTO reserva VALUES('" + nome_aluno + "','" + nome_livro + "','" + curso + "','"+data+"');", "INSERT");
            JOptionPane.showMessageDialog(this, "Reservado com sucesso!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jRadioButton4.setSelected(false);
            jRadioButton5.setSelected(false);
            jRadioButton6.setSelected(false);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

